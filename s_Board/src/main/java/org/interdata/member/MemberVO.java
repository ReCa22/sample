package org.interdata.member;

public class MemberVO {
	
	private int rnum;
	private String id;
	private String pw;
	private String name;
	private String email;
	private int postcode1;
	private int postcode2;
	private String address;
	private String address_etc;
	private int gender;
	private int question;
	private String answer;
	
	public int getRnum() {
		return rnum;
	}
	public void setRnum(int rnum) {
		this.rnum = rnum;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public int getPostcode1() {
		return postcode1;
	}
	public void setPostcode1(int postcode1) {
		this.postcode1 = postcode1;
	}
	public int getPostcode2() {
		return postcode2;
	}
	public void setPostcode2(int postcode2) {
		this.postcode2 = postcode2;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getAddress_etc() {
		return address_etc;
	}
	public void setAddress_etc(String address_etc) {
		this.address_etc = address_etc;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public int getQuestion() {
		return question;
	}
	public void setQuestion(int question) {
		this.question = question;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}

}
